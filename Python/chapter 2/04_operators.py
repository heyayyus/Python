# Arithmetic Operators are used to perform mathematical operations like addition, subtraction, multiplication etc.
a = 7
b = 4
c = a + b
print(c)



# Assignment Operators are used to assign values to variables.
a = 4-2 # Assign 4-2 in a
print(a)

b = 6
# b += 3 # Increment the value of b by 3 and then assign it to b
b -= 3 # Decrement the value of b by 3 and then assign it to b
print(b)


# Comparison Operators are used to compare two values
d = 5==5
print(d)


# = means assignment  == means comparison     != means not equal to   > means greater than     < means less than     >= means greater than or equal to     <= means less than or equal to




# Logical Operators are used to combine conditional statements
e = True or False

# Truth table of 'or' 
print("True or False is ", True or False)
print("True or True is ", True or True)
print("False or True is ", False or True)
print("False or False is ", False or False)

# Truth table of 'and' 
print("True and False is ", True and False)
print("True and True is ", True and True)
print("False and True is ", False and True)
print("False and False is ", False and False)


# not operator is used to reverse the logical state of its operand  (True to False and False to True)

print(not(True))

